public class veiculo {

    protected String marca;
    protected String modelo;
    protected String cor;
    protected String placa;
    protected int n_roda;
    protected String tracao;

    public veiculo (String marca, String modelo, String cor, String placa, int n_roda, String tracao){

        this.marca = marca;
        this.modelo = modelo;
        this.cor = cor;
        this.placa = placa;
        this.n_roda = n_roda;
        this.tracao = tracao;

    }

}
