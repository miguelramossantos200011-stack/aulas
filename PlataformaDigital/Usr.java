public class Usr {

    private String UsrNM;
    private String UsrMail;

    public Usr(String UsrNM, String UsrMail){

        this.UsrNM = UsrNM;
        this.UsrMail = UsrMail;

    }

    public String getUsrNM() {

        return this.UsrNM;

    }

    public String getUsrMail() {

        return this.UsrMail;

    }

}
